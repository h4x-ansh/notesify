/**
 * One-off asset generator: rasterizes the feTurbulence SVG noise filter
 * (previously recomputed live on every page, every render) into a small
 * tileable PNG, saved as a base64 data URI. Run this only when the noise
 * look needs to change — the output is checked in as a static asset.
 *
 *   node scripts/generate-noise-texture.mjs
 */
import puppeteer from "puppeteer";
import { writeFile } from "node:fs/promises";

const TILE_SIZE = 200;

const svgHtml = `<!DOCTYPE html>
<html><head><style>
  html, body { margin: 0; padding: 0; }
  svg { display: block; }
</style></head>
<body>
  <svg viewBox="0 0 ${TILE_SIZE} ${TILE_SIZE}" width="${TILE_SIZE}" height="${TILE_SIZE}" xmlns="http://www.w3.org/2000/svg">
    <filter id="n"><feTurbulence type="fractalNoise" baseFrequency="0.65" numOctaves="3" stitchTiles="stitch"/></filter>
    <rect width="100%" height="100%" filter="url(#n)"/>
  </svg>
</body></html>`;

const browser = await puppeteer.launch({ headless: true, args: ["--no-sandbox", "--disable-setuid-sandbox"] });
try {
  const page = await browser.newPage();
  await page.setViewport({ width: TILE_SIZE, height: TILE_SIZE });
  await page.setContent(svgHtml, { waitUntil: "load" });
  const buffer = await page.screenshot({ type: "png" });
  const dataUri = `data:image/png;base64,${buffer.toString("base64")}`;

  const out = `/** Auto-generated by scripts/generate-noise-texture.mjs — do not hand-edit. */\nexport const NOISE_TEXTURE_DATA_URI = "${dataUri}";\n`;
  await writeFile(new URL("../src/templates/noiseTexture.js", import.meta.url), out, "utf-8");
  console.log(`Wrote src/templates/noiseTexture.js (${(buffer.length / 1024).toFixed(1)} KB PNG, ${TILE_SIZE}x${TILE_SIZE})`);
} finally {
  await browser.close();
}
